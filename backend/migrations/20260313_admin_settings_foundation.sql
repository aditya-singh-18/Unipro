-- Admin settings foundation: system sections, project types, tracks, and technologies

CREATE TABLE IF NOT EXISTS admin_settings (
  section_key VARCHAR PRIMARY KEY,
  setting_value JSONB NOT NULL,
  updated_by VARCHAR REFERENCES users(user_key) ON DELETE SET NULL,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_admin_settings_updated_at
  ON admin_settings (updated_at DESC);

CREATE TABLE IF NOT EXISTS project_types (
  project_type_key VARCHAR PRIMARY KEY,
  display_name VARCHAR(120) NOT NULL,
  description TEXT,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  allowed_semesters JSONB NOT NULL DEFAULT '[]'::jsonb,
  min_team_size INT NOT NULL DEFAULT 1,
  max_team_size INT NOT NULL DEFAULT 4,
  default_total_weeks INT NOT NULL DEFAULT 12,
  form_open_at TIMESTAMP NULL,
  form_close_at TIMESTAMP NULL,
  project_start_date DATE NULL,
  project_end_date DATE NULL,
  allow_solo BOOLEAN NOT NULL DEFAULT FALSE,
  requires_github BOOLEAN NOT NULL DEFAULT FALSE,
  allow_custom_tech BOOLEAN NOT NULL DEFAULT TRUE,
  tracker_template JSONB NOT NULL DEFAULT '{}'::jsonb,
  sort_order INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_by VARCHAR NULL REFERENCES users(user_key) ON DELETE SET NULL,
  CONSTRAINT chk_project_type_team_bounds CHECK (min_team_size >= 1 AND max_team_size >= min_team_size),
  CONSTRAINT chk_project_type_weeks CHECK (default_total_weeks BETWEEN 1 AND 52)
);

CREATE INDEX IF NOT EXISTS idx_project_types_active_sort
  ON project_types (is_active DESC, sort_order ASC, display_name ASC);

CREATE TABLE IF NOT EXISTS track_catalog (
  track_key VARCHAR PRIMARY KEY,
  display_name VARCHAR(120) NOT NULL,
  description TEXT,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  allow_custom_tech BOOLEAN NOT NULL DEFAULT FALSE,
  sort_order INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_by VARCHAR NULL REFERENCES users(user_key) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_track_catalog_active_sort
  ON track_catalog (is_active DESC, sort_order ASC, display_name ASC);

CREATE TABLE IF NOT EXISTS track_technologies (
  technology_id BIGSERIAL PRIMARY KEY,
  track_key VARCHAR NOT NULL REFERENCES track_catalog(track_key) ON DELETE CASCADE,
  technology_name VARCHAR(160) NOT NULL,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  sort_order INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_by VARCHAR NULL REFERENCES users(user_key) ON DELETE SET NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_track_technology_name
  ON track_technologies (track_key, LOWER(BTRIM(technology_name)));

CREATE INDEX IF NOT EXISTS idx_track_technologies_active_sort
  ON track_technologies (track_key ASC, is_active DESC, sort_order ASC, technology_name ASC);

